const quotes = ["Rejection is God's protection", "Be deliberate about your happiness", "Dreams are not just enough", 
                "No man is your friend, No man is your enemy, But every man is your teacher"]

let quote = 0
let index = 0
let currentText = ""
let letter = ""

const type = ()=>{
    if(quote === quotes.length){
        quote = 0
    }
    currentText = quotes[quote]
    letter = currentText.slice(0, ++index)
    document.querySelector(".typing").textContent = letter
    if(letter.length === currentText.length){
        quote++;
        index = 0
    }
    setTimeout(type, 200)
}
type()




































































































































































































































































































































































































































































































































































